日前，中共中央办公厅、国务院办公厅印发《关于深化新时代学校思想政治理论课改革创新的若干意见》。为深入实施意见，向各地各校做好宣传动员工作，教育部负责人就意见制定实施回答了记者提问。
 </p>  
