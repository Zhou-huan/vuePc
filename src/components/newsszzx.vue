     </p> 
     <p>一是课程目标方面，强调在大中小学循序渐进、螺旋上升地开设思政课。</p>
<p>二是课程体系方面，提出加强以习近平新时代中国特色社会主义思想为核心内容的思政课课程群建设，全国重点马克思主义学院率先全面开设“习近平新时代中国特色社会主义思想概论”课，构建思政课“必修课+选修课”的课程体系。</p>
<p>三是课程内容方面，进一步明确要坚持用习近平新时代中国特色社会主义思想铸魂育人，以政治认同、家国情怀、道德修养、法治意识、文化素养为重点，以爱党、爱国、爱社会主义、爱人民、爱集体为主线，坚持爱国和爱党爱社会主义相统一，系统开展马克思主义理论教育，系统进行中国特色社会主义和中国梦教育、社会主义核心价值观教育、法治教育、劳动教育、心理健康教育、中华优秀传统文化教育。
</p>
<p>四是教材建设方面，提出编写教学指南、示范教案，出版优秀讲义，开列典籍书单，建设网络教学资源库等新举措。</p>
          
       <p>  
 <span class="newstitle">问：意见对建设思政课教师队伍提出哪些新举措？</span>


         </p>

         
       <p>
