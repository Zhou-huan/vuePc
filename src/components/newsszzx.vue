</p>    
 
<p>一是壮大教师队伍，提出各地在核定编制时要充分考虑思政课教师配备要求；高校要严格按照师生比不低于1︰350的比例核定专职思政课教师岗位，在编制内配足，且不得挪作他用，并尽快配备到位；各地应对民办学校指派思政课教师或组建专门讲师团。

</p>   

               <p>
二是提高教师综合素质，提出举办学习习近平新时代中国特色社会主义思想专题研修班；组织开设高校思政课教师“周末理论大讲堂”；建立一批“新时代高校思政课教师研学基地”等。          </p> 
          <p>
三是切实改革教师评价机制，在专业技术职务（职称）评聘工作中要单独设立马克思主义理论类别；要将思政课教师在中央和地方主要媒体上发表的理论文章纳入学术成果范畴；实行不合格思政课教师退出机制。
              </p> 

              <p>
四是努力加大思政课教师激励力度，把思政课教师和辅导员中的优秀分子纳入各类高层次人才项目；各地要因地制宜设立思政课教师和辅导员岗位津贴，纳入绩效工资管理，相应核增学校绩效工资总量等。                  
                  </p> 



